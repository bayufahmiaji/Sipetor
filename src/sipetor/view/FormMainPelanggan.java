    private void tblPelangganMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPelangganMousePressed
         if(evt.getClickCount()==2){
            int idxrow = tblPelanggan.getSelectedRow();
            txtTnkb.setText(tblPelanggan.getModel().getValueAt(idxrow, 0).toString());
            TxtPelanggan.setText(tblPelanggan.getModel().getValueAt(idxrow, 1).toString());
            txtAlamat.setText(tblPelanggan.getModel().getValueAt(idxrow, 2).toString());
            btnSimpan.setText("Update");
        }
        if(evt.isPopupTrigger()){
            ppPelanggan.show(tblPelanggan, evt.getX(), evt.getY());
        }
    }//GEN-LAST:event_tblPelangganMousePressed

