    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String tnkb = txtTnkb.getText();
        String nama = TxtPelanggan.getText();
        String alamat = txtAlamat.getText();
        
        Pelanggan data = new Pelanggan(tnkb, nama, alamat);
        
        if(btnSimpan.getText().equals("Simpan")){
            if(controllerPelanggan.insertPelanggan(data)){
                outCommand.setText("Insert Berhasil");
            }else{
                outCommand.setText("Insert Gagal");
            }
        }else{
            if(controllerPelanggan.updatePelanggan(data)){
                outCommand.setText("Update Berhasil");
            }else{
                outCommand.setText("Upate Gagal");
            }
        }
        setClear();
    }//GEN-LAST:event_btnSimpanActionPerformed

