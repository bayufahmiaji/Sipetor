    private void mnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnDeleteActionPerformed
         int result = JOptionPane.showConfirmDialog(rootPane, "Apakah Yakin Untuk Didelete?","Delete Dialog",JOptionPane.YES_NO_OPTION);
        if(result == JOptionPane.YES_OPTION){
            String id = tblPelanggan.getModel().getValueAt(tblPelanggan.getSelectedRow(), 0).toString();
            controllerPelanggan.deletePelanggan(id);
            setClear();
        }
    }//GEN-LAST:event_mnDeleteActionPerformed

    private void setTablePelanggan(List<Pelanggan> listPelanggan){
        if(listPelanggan==null){
            JOptionPane.showMessageDialog(this,"Data Karyawan Gagal Diambil Dari Database",
                    "DATABASE FAILED",JOptionPane.ERROR_MESSAGE);
        }else{
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(new String[]{
                "TNKB",
                "Nama Pelanggan",
                "Alamat Pelanggan"
            });
            
            for(Pelanggan p : listPelanggan){
                Object[] o = new Object[3];
                o[0] = p.getTnkb();
                o[1] = p.getNama_Pelanggan();
                o[2] = p.getAlamat_Pelanggan();
                tableModel.addRow(o);
        }
            tblPelanggan.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS);
            tblPelanggan.setModel(tableModel);
    }
    }
    private void setClear(){
        txtTnkb.setText("");
        TxtPelanggan.setText("");
        txtAlamat.setText("");
        btnSimpan.setText("Simpan");
        setTablePelanggan(controllerPelanggan.getAllData());
    }
